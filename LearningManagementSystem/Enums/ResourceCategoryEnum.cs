namespace LearningManagementSystem.Enums
{
    /// <summary>
    /// Enum representing different categories of resources in the Learning Management System.
    /// </summary>
    public enum ResourceCategoryEnum
    {
        /// <summary>
        /// Represents an assignment resource.
        /// </summary>
        Assignment = 1,

        /// <summary>
        /// Represents a notification resource.
        /// </summary>
        Notification = 2,

        /// <summary>
        /// Represents a document resource.
        /// </summary>
        Document = 3,

        /// <summary>
        /// Represents an exam resource.
        /// </summary>
        Exam = 4,

        /// <summary>
        /// Represents any other type of resource not covered by the other categories.
        /// </summary>
        Other = 5
    }

    
}
